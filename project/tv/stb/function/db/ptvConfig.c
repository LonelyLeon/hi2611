static void tvconf_save_in_rec(D_U32 offset, D_U32 data_size)
{
#ifdef D_SUPPORT_EEPROM
    d_dbase_eeprom_store_data (&config_blk, D_TVCONF_OFFSET_IN_TBL + offset, data_size);
#else
    if(config_blk.store)
    {
        DIRTY();
        d_dbase_record_flash_flush_blk(config_block_id, (D_TVCONF_OFFSET_IN_TBL + offset), data_size, (((D_U8*)tv_config) + offset), 1);
    }
#endif
}

/** @brief 恢复缺省配置信息*/
static D_Result
tvconfig_restore (D_Bool update, D_Bool fully) D_FUNCPOSTFIT
{
    D_DUMP ("Restore default tvconfig\n");

    LOCK();

    if(fully == 1)
    {
        /*第一次开机初始化*/
        memcpy(tv_config, (D_U8*)&def_tv_config, sizeof(D_Tv_Config));
        TVCONFIG.debug_en = 1;
    }
    else if(fully == 2)
    {
        /*清eep*/
        memcpy(tv_config, (D_U8*)&def_tv_config, sizeof(D_Tv_Config));
    }

    else
    {
        D_CTEMP_SET  ctemp_set[D_DB_MODE_NUM][TV_COLOR_MODE_MAX];
#ifndef D_TV_IAMGE_SOURCE
        D_ADC_SET    adc_set[D_DB_MODE_NUM];
        D_ADC_SET    hdadc_set[D_DB_MODE_NUM];
#endif
        D_U32 i;
        for(i = 0; i < D_DB_MODE_NUM; i++)
        {
            memcpy((D_U8*)(&(ctemp_set[i][0])), (D_U8*) & (TVCONFIG.sub[i].ctemp_set[0]), sizeof(D_CTEMP_SET)*TV_COLOR_MODE_MAX);
#ifndef D_TV_IAMGE_SOURCE
            memcpy((D_U8*)(&adc_set[i]), (D_U8*) & (TVCONFIG.sub[i].adc_set), sizeof(D_ADC_SET));
            memcpy((D_U8*)(&hdadc_set[i]), (D_U8*) & (TVCONFIG.sub[i].hdadc_set), sizeof(D_ADC_SET));
#endif
        }
        /*默认参数*/
        memcpy(tv_config, (D_U8*)&def_tv_config, sizeof(D_Tv_Config));
        for(i = 0; i < D_DB_MODE_NUM; i++)
        {
            memcpy((D_U8*) & (TVCONFIG.sub[i].ctemp_set[0]), (D_U8*)(&(ctemp_set[i][0])), sizeof(D_CTEMP_SET)*TV_COLOR_MODE_MAX);
#ifndef D_TV_IAMGE_SOURCE
            memcpy((D_U8*) & (TVCONFIG.sub[i].adc_set), (D_U8*)(&adc_set[i]), sizeof(D_ADC_SET));
            memcpy((D_U8*) & (TVCONFIG.sub[i].hdadc_set), (D_U8*)(&hdadc_set[i]), sizeof(D_ADC_SET));
#endif
        }
    }

    tvconf_save_in_rec(0, sizeof(D_Tv_Config));

    UNLOCK();

    return D_OK;
}

/** @brief 初始化配置信息*/
D_Result
p_tvconf_init (void) D_FUNCPOSTFIT
{
    /* 请保持在512字节内 */
    if((sizeof(D_Tv_Config) + D_TVCONF_OFFSET_IN_TBL) > P_CONFIG_BLOCK_SIZE)
    {
        D_ERROR("Please make sure sizeof 'D_Tv_Config' !\n");
        return D_ERR;
    }

    config_block_id = d_dbase_get_block_id((D_DBBlock*)(&config_blk));

    tv_config = (D_Tv_Config*)(config_blk.buf + D_TVCONF_OFFSET_IN_TBL);
    D_DUMP("Size of struct D_Tv_Config is %d bytes!\n", sizeof(D_Tv_Config));
#ifdef D_TV_IAMGE_SOURCE
    def_tv_config.flag_valid = (((D_U32)(sizeof(D_Tv_Config))) << 16) + ((D_U16)D_MANUFACTORY_ID);
#endif
    if(TVCONFIG.flag_valid != def_tv_config.flag_valid)
    {
        tvconfig_restore(D_FALSE, 1);
        /* 第一次开机预置节目 */
        p_precast_program(1);
    }

    return D_OK;
}

/** @brief 恢复为最初配置信息*/
D_Result
p_tvconf_restore (D_Bool update) D_FUNCPOSTFIT
{
    tvconfig_restore(update, 0);

    return D_OK;
}

