/** @brief 设置wifi ap 参数*/
D_Result p_tvconf_set_wifiap_param  (D_WifiApConfig *param) D_FUNCPOSTFIT
{
#ifdef D_TV_SUPPORT_WIFI
    D_S32 idx, sidx, sel_idx;
    D_S32 ver_max, ver_min;
    D_Bool save;
    if( ! param)
    {
        return D_ERR_INVALID_ARGUMENT;
    }
    LOCK ();
    save = D_TRUE;
    sidx = -1;
    sel_idx = -1;
    ver_max = 0x0;
    ver_min = 0x10000;
    param->valid_magic = D_TV_NETWORK_VALID_MAGIC;
    for(idx = 0; idx < D_TV_WIFI_AP_CNT; idx++)
    {
        if(TVCONFIG.wifiapconfig[idx].valid_magic == D_TV_NETWORK_VALID_MAGIC)
        {
            ver_max = D_MAX((D_S32)(TVCONFIG.wifiapconfig[idx].version), ver_max);
            if((D_S32)(TVCONFIG.wifiapconfig[idx].version) < ver_min)
            {
                sidx = idx;   /*记录最低版本位置*/
                ver_min = D_MIN((D_S32)(TVCONFIG.wifiapconfig[idx].version), ver_min);
            }
        }
        else
        {
            if(sel_idx < 0)
            {
                sel_idx = idx; /*记录第一个空闲位置*/
            }
            ver_min = 0;
        }
    }

    sel_idx = (sel_idx < 0) ? sidx : sel_idx;   /*记录可能要存储的位置*/
    ver_min = (ver_min > ver_max) ? ver_max : ver_min;
    D_DUMP("pconfig wifiap   ver_max : %04x, ver_min: %04x\n", ver_max , ver_min);
    sidx = -1;
    for(idx = 0; idx < D_TV_WIFI_AP_CNT; idx++)
    {
        if(TVCONFIG.wifiapconfig[idx].valid_magic == D_TV_NETWORK_VALID_MAGIC && memcmp(param->bssid, TVCONFIG.wifiapconfig[idx].bssid, sizeof(param->bssid)) == 0)
        {
            if((strcmp(param->essid, TVCONFIG.wifiapconfig[idx].essid) == 0) && param->key_len == TVCONFIG.wifiapconfig[idx].key_len && memcmp(param->key, TVCONFIG.wifiapconfig[idx].key, sizeof(TVCONFIG.wifiapconfig[idx].key)) == 0)
            {
                if(ver_max <= (D_S32)(TVCONFIG.wifiapconfig[idx].version))
                {
                    save = D_FALSE;  /*版本最高了，就不再操作。否则更新版本*/
                }
            }
            sidx = idx; /*如果ap相同，则直接存储在此位置*/
            break;
        }
    }

    if(save)
    {
        sidx = (sidx < 0) ? sel_idx : sidx;
        sidx = (sidx < 0 || sidx >= D_TV_WIFI_AP_CNT) ? 0 : sidx;
        if(TVCONFIG.wifiapconfig[sidx].valid_magic != D_TV_NETWORK_VALID_MAGIC || (TVCONFIG.wifiapconfig[sidx].valid_magic == D_TV_NETWORK_VALID_MAGIC && TVCONFIG.wifiapconfig[sidx].version != ver_max))
        {
            ver_max++;
            ver_max = (ver_max > 0xFFFF) ? 1 : ver_max;
        }
        D_DUMP("pconfig wifiap  save %d ver_max : %04x\n", sidx, ver_max);
        memcpy (&TVCONFIG.wifiapconfig[sidx], param, sizeof (D_WifiApConfig));
        TVCONFIG.wifiapconfig[sidx].valid_magic = D_TV_NETWORK_VALID_MAGIC;
        TVCONFIG.wifiapconfig[sidx].version = (D_U16)ver_max;
        tvconf_save_in_rec((D_U8*)&TVCONFIG.wifiapconfig[sidx] - (D_U8*)&TVCONFIG, sizeof(D_WifiApConfig));
    }
    UNLOCK ();
#endif
    return D_OK;
}

